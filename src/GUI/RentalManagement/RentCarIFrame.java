    private void newCustomerRentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_newCustomerRentButtonActionPerformed
        // TODO add your handling code here:
        String idInput = newCustomerIdTextField.getText();
        String ageInput = ageTextField.getText();
        boolean validId = Utility.checkIntegerInput(idInput);
        boolean validAge = Utility.checkIntegerInput(ageInput);
        if(validId == true && validAge == true){
            Hashtable<String,String> data = Utility.readData(newCustomerPanel);
            int id = Integer.parseInt(newCustomerIdTextField.getText());
            int age = Integer.parseInt(ageTextField.getText());
            Date licenceExpiry = licenceExpiryDatePicker.getDate();
            Date dob = birthDatePicker.getDate();
            Date dueDate = newCustomerDatePicker.getDate();
            
            String output = Utility.checkEmptyField(data);
            if (output.length() != 0){
                JOptionPane.showMessageDialog(this, output);
            }
            
            else{
                Customer cust = new Customer(data.get("Licence Number"), age, licenceExpiry, id, data.get("Name"), dob, data.get("Address"), data.get("Telephone"));
                employee.addPerson(store, cust);
                Car car = employee.searchCarByRegistrationNumber(store, data.get("Car Registration Number"));
                Person person = employee.searchPersonById(id, store);
                if (car == null){
                    JOptionPane.showMessageDialog(this, "No car found with the registration number entered");
                }
                else if (person == null){
                    JOptionPane.showMessageDialog(this, "No customer found with the ID entered");
                }
                else{
                    if(car.getStatus() == Status.Available){
                        if(person instanceof Customer){
                            Customer customer = (Customer) person;
                            Rental rental = new Rental(car, customer, dueDate);
                            employee.rentCar(rental, store, car, customer);
                            JOptionPane.showMessageDialog(this, "Car rented successfully");
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "No customer found with the ID entered");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Sorry this car is not available for renting");
                    }
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Please enter only numbers for age and ID fields");
        }
    }//GEN-LAST:event_newCustomerRentButtonActionPerformed


