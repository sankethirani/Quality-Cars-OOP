    private void ExistingCustomerRentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExistingCustomerRentButtonActionPerformed
        // TODO add your handling code here:
        String idInput = existingCustomerIdTextField.getText();
        boolean validId = Utility.checkIntegerInput(idInput);
        if (validId == true){
            boolean emptyField = true;
            String registrationNumberInput = existingCustomerCarRegistrationNumberTextField.getText();
            if (registrationNumberInput.length() == 0){
                JOptionPane.showMessageDialog(this, "Please enter car registration number");
            }
            else{
                emptyField = false;
            }

            if(emptyField == false){
                int id = Integer.parseInt(existingCustomerIdTextField.getText());
                String registrationNumber = existingCustomerCarRegistrationNumberTextField.getText();
                Date dueDate = existingCustomerDueDatePicker.getDate();

                Car car = employee.searchCarByRegistrationNumber(store, registrationNumber);
                Person person = employee.searchPersonById(id, store);
                if (car == null){
                    JOptionPane.showMessageDialog(this, "No car found with the registration number entered");
                }
                else if (person == null){
                    JOptionPane.showMessageDialog(this, "No customer found with the ID entered");
                }
                else{
                    if(car.getStatus() == Status.Available){
                        if(person instanceof Customer){
                            Customer customer = (Customer) person;
                            Rental rental = new Rental(car, customer, dueDate);
                            employee.rentCar(rental, store, car, customer);
                            JOptionPane.showMessageDialog(this, "Car rented successfully");
                        }
                        else{
                            JOptionPane.showMessageDialog(this, "No customer found with the ID entered");
                        }
                    }
                    else{
                        JOptionPane.showMessageDialog(this, "Sorry this car is not available for renting");
                    }
                }
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Please enter a number for customer ID");
        }
        
    }//GEN-LAST:event_ExistingCustomerRentButtonActionPerformed

